# I am creating a list of group to store the definition of "fruit" within itself as data
fruit = ["apples", "pears", "oranges"]

# This is a loop, the verb is the word (for) it is telling the computer
# it is about to loop or iterate over a list. When the group of "fruit"
# has no more values, the computer will stop looping
for currentFruit in fruit:

    print("The current fruit in this list is " + currentFruit)